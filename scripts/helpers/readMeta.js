import fs from 'node:fs';
import {WIKI_META_FILE_NAME} from './SETTINGS.js';

// =====================================================================================================================
//  P U B L I C
// =====================================================================================================================
/**
 *
 */
function readMeta(dirPath) {
    try {
        return JSON.parse(fs.readFileSync(dirPath + '/' + WIKI_META_FILE_NAME, 'utf8'));
    } catch (e) {
        return {
            lastUpdate: null,
            pages: {},
        };
    }
}

// =====================================================================================================================
//  E X P O R T
// =====================================================================================================================
export default readMeta;
